# int
num_1 = 2 + 4
print("num_1", type(num_1))
print("*************************************")

# float
num_2 = 2 / 4
print("num_2", type(num_2))

# これもfloat
num_2 = 4 / 2
print("num_2", type(num_2))
print("*************************************")

# **
num_3 = 2 ** 4
print("num_3", num_3)

# %
num_4 = 4 % 2
print("num_4", num_4)
print("num_4", type(num_4))
print("*************************************")

# //
print("//", 17 // 3)
print("*************************************")

# round
